import React from 'react';
import { useBuilder } from '../BuilderContext';
import APIError from '../../../components/common/APIError';
import SkeletonLoader from '../../../components/common/SkeletonLoader';
import BuilderToolbar from './BuilderToolbar';
import QuestionBankPanel from './QuestionBankPanel';
import SelectedQuestionsPanel from './SelectedQuestionsPanel';
import PaperSummaryCard from './PaperSummaryCard';
import ReplaceQuestionDialog from './ReplaceQuestionDialog';
import InternalChoiceDialog from './InternalChoiceDialog';
import AutoGenerateDialog from './AutoGenerateDialog';
import GenerationProgressDialog from './GenerationProgressDialog';

function BuilderLayout() {
  const { loading, error, paper, fetchData, isReplaceModalOpen, isInternalChoiceModalOpen } = useBuilder();

  if (loading) {
    return (
      <div style={{padding: '40px'}}>
        <SkeletonLoader lines={8} height="60px" />
      </div>
    );
  }

  if (error) {
    return (
      <div style={{padding: '40px'}}>
        <APIError error={error} onRetry={fetchData} resourceName="Builder Data" />
      </div>
    );
  }

  if (!paper) {
    return <div className="builder-error">Question Paper not found.</div>;
  }

  return (
    <div className="builder-container">
      <BuilderToolbar />
      <PaperSummaryCard />
      
      <div className="builder-main">
        <QuestionBankPanel />
        <SelectedQuestionsPanel />
      </div>

      {isReplaceModalOpen && <ReplaceQuestionDialog />}
      {isInternalChoiceModalOpen && <InternalChoiceDialog />}
      <AutoGenerateDialog />
      <GenerationProgressDialog />
    </div>
  );
}

export default BuilderLayout;
