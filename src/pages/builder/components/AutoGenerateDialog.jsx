import React, { useState, useEffect } from 'react';
import { useBuilder } from '../BuilderContext';

function AutoGenerateDialog() {
  const { 
    isAutoGenerateModalOpen, setAutoGenerateModalOpen, 
    paper, sections, executeAutoGenerate 
  } = useBuilder();

  // Local state for the configuration form, cloned from the builder sections
  const [localSections, setLocalSections] = useState([]);

  useEffect(() => {
    if (isAutoGenerateModalOpen) {
      setLocalSections(JSON.parse(JSON.stringify(sections)));
    }
  }, [isAutoGenerateModalOpen, sections]);

  if (!isAutoGenerateModalOpen) return null;

  const updateSectionConfig = (sectionIndex, field, value) => {
    const newSecs = [...localSections];
    
    if (field.includes('.')) {
      const [parent, child] = field.split('.');
      newSecs[sectionIndex].config[parent][child] = parseInt(value) || 0;
    } else {
      newSecs[sectionIndex].config[field] = value;
    }
    
    setLocalSections(newSecs);
  };

  const getSectionMarks = (conf) => {
    const qCount = conf.internal_choice === 'Yes' ? (parseInt(conf.optional_questions) || 0) : (parseInt(conf.num_questions) || 0);
    return qCount * (parseFloat(conf.marks_per_question) || 0);
  };

  const totalPaperMarks = localSections.reduce((sum, sec) => sum + getSectionMarks(sec.config), 0);

  const handleGenerate = () => {
    // Validate everything first
    for (let sec of localSections) {
      const diffSum = Object.values(sec.config.diffDist).reduce((a,b)=>a+(parseInt(b)||0), 0);
      if (diffSum !== 100) return alert(`${sec.name} Difficulty must equal 100%`);
      
      const bloomSum = Object.values(sec.config.bloomDist).reduce((a,b)=>a+(parseInt(b)||0), 0);
      if (bloomSum !== 100) return alert(`${sec.name} Bloom's must equal 100%`);
      
      // Ensure section total_marks is updated before passing to backend
      sec.total_marks = getSectionMarks(sec.config);
    }
    
    executeAutoGenerate(localSections);
  };

  return (
    <div className="modal-overlay">
      <div className="modal-content" style={{width: '1100px', maxWidth:'95vw', height: '90vh', display: 'flex', flexDirection: 'column'}}>
        {/* HEADER */}
        <div className="modal-header" style={{background: 'white', borderBottom: '1px solid #e2e8f0', padding: '15px 25px'}}>
          <div style={{display:'flex', flexDirection:'column', gap:'4px'}}>
            <h3 style={{margin: 0, fontSize: '1.25rem', color: '#0f172a'}}>Auto Generate Paper</h3>
            <div style={{fontSize: '0.85rem', color: '#64748b', fontWeight:'500'}}>
              {paper.course} • {paper.program} | Semester {paper.semester} • {paper.exam_type} • 
              Target: {paper.total_marks} Marks | <span style={{color: totalPaperMarks === paper.total_marks ? '#16a34a' : '#ef4444', fontWeight: 'bold'}}>Configured: {totalPaperMarks} Marks</span>
            </div>
          </div>
          <div style={{display:'flex', gap:'15px', alignItems:'center'}}>
            <div style={{display:'flex', alignItems:'center', gap:'8px', fontSize:'0.85rem', fontWeight:'600', color:'#475569'}}>
              Generation Method 
              <select className="styled-input" style={{padding:'6px 12px', width:'200px'}} disabled>
                <option>Rule Based (Standard)</option>
              </select>
            </div>
            <button className="btn-icon" onClick={() => setAutoGenerateModalOpen(false)}>✕</button>
          </div>
        </div>
        
        {/* BODY */}
        <div className="modal-body" style={{flex: 1, overflowY: 'auto', background: '#f8fafc', padding: '25px'}}>
          <div style={{display: 'flex', flexDirection: 'column', gap: '25px'}}>
          {localSections.map((sec, idx) => {
            const conf = sec.config;
            const diffSum = (conf.diffDist.Easy||0) + (conf.diffDist.Medium||0) + (conf.diffDist.Hard||0);
            const bloomSum = (conf.bloomDist.Remember||0) + (conf.bloomDist.Understand||0) + (conf.bloomDist.Apply||0) + 
                             (conf.bloomDist.Analyze||0) + (conf.bloomDist.Evaluate||0) + (conf.bloomDist.Create||0);
            
            return (
              <div key={sec.client_id} style={{background: 'white', borderRadius: '12px', padding: '25px', boxShadow: '0 4px 6px -1px rgba(0,0,0,0.05), 0 2px 4px -1px rgba(0,0,0,0.03)', border:'1px solid #f1f5f9'}}>
                <div style={{display:'flex', justifyContent:'space-between', alignItems:'center', borderBottom:'1px solid #e2e8f0', paddingBottom:'12px', marginBottom:'20px'}}>
                  <h4 style={{margin:0, color:'#0f172a', fontSize:'1.1rem'}}>{sec.name}</h4>
                  <span style={{fontWeight:'bold', color:'#3b82f6', background:'#eff6ff', padding:'4px 12px', borderRadius:'20px', fontSize:'0.9rem'}}>{getSectionMarks(conf)} Marks</span>
                </div>

                <div style={{display:'grid', gridTemplateColumns:'repeat(auto-fit, minmax(180px, 1fr))', gap:'20px', marginBottom:'25px'}}>
                  <div className="form-group">
                    <label className="form-label">Question Type</label>
                    <select className="styled-input" value={conf.question_type} onChange={e => updateSectionConfig(idx, 'question_type', e.target.value)}>
                      <option>Mixed</option><option>MCQ</option><option>Short Answer</option><option>Long Answer</option><option>Numerical</option>
                    </select>
                  </div>
                  <div className="form-group">
                    <label className="form-label">Questions</label>
                    <input type="number" className="styled-input" value={conf.num_questions} onChange={e => updateSectionConfig(idx, 'num_questions', e.target.value)} />
                  </div>
                  <div className="form-group">
                    <label className="form-label">Marks / Question</label>
                    <input type="number" className="styled-input" value={conf.marks_per_question} onChange={e => updateSectionConfig(idx, 'marks_per_question', e.target.value)} />
                  </div>
                  <div className="form-group">
                    <label className="form-label">Internal Choice</label>
                    <select className="styled-input" value={conf.internal_choice} onChange={e => updateSectionConfig(idx, 'internal_choice', e.target.value)}>
                      <option>No</option><option>Yes</option>
                    </select>
                  </div>
                </div>

                {conf.internal_choice === 'Yes' && (
                  <div className="form-group" style={{marginBottom:'25px', padding:'15px', background:'#eff6ff', borderRadius:'8px', border:'1px solid #bfdbfe', width:'fit-content'}}>
                    <label className="form-label" style={{color:'#1e3a8a'}}>Number of OR Pairs required (e.g. 2 choices out of the {conf.num_questions})</label>
                    <input type="number" className="styled-input" style={{width:'150px'}} value={conf.optional_questions} onChange={e => updateSectionConfig(idx, 'optional_questions', e.target.value)} />
                  </div>
                )}

                <div style={{display:'grid', gridTemplateColumns:'1fr 2fr', gap:'25px', marginBottom:'25px'}}>
                  {/* Difficulty */}
                  <div className="config-block">
                    <div className="config-block-header">
                      <span className="form-label">Difficulty Total</span>
                      <span className={`validation-badge ${diffSum === 100 ? 'valid' : 'invalid'}`}>
                        {diffSum}% {diffSum === 100 ? '✅ Perfect' : '❌ Must equal 100%'}
                      </span>
                    </div>
                    <div style={{display:'grid', gridTemplateColumns:'1fr 1fr 1fr', gap:'15px'}}>
                      <div className="form-group">
                        <label className="form-label-small">Easy</label>
                        <input type="number" className="styled-input" value={conf.diffDist.Easy} onChange={e => updateSectionConfig(idx, 'diffDist.Easy', e.target.value)} />
                      </div>
                      <div className="form-group">
                        <label className="form-label-small">Medium</label>
                        <input type="number" className="styled-input" value={conf.diffDist.Medium} onChange={e => updateSectionConfig(idx, 'diffDist.Medium', e.target.value)} />
                      </div>
                      <div className="form-group">
                        <label className="form-label-small">Hard</label>
                        <input type="number" className="styled-input" value={conf.diffDist.Hard} onChange={e => updateSectionConfig(idx, 'diffDist.Hard', e.target.value)} />
                      </div>
                    </div>
                  </div>

                  {/* Bloom */}
                  <div className="config-block">
                    <div className="config-block-header">
                      <span className="form-label">Bloom's Total</span>
                      <span className={`validation-badge ${bloomSum === 100 ? 'valid' : 'invalid'}`}>
                        {bloomSum}% {bloomSum === 100 ? '✅ Perfect' : '❌ Must equal 100%'}
                      </span>
                    </div>
                    <div style={{display:'grid', gridTemplateColumns:'repeat(auto-fit, minmax(70px, 1fr))', gap:'12px'}}>
                      <div className="form-group"><label className="form-label-small">Remember</label><input type="number" className="styled-input" value={conf.bloomDist.Remember} onChange={e => updateSectionConfig(idx, 'bloomDist.Remember', e.target.value)} /></div>
                      <div className="form-group"><label className="form-label-small">Understand</label><input type="number" className="styled-input" value={conf.bloomDist.Understand} onChange={e => updateSectionConfig(idx, 'bloomDist.Understand', e.target.value)} /></div>
                      <div className="form-group"><label className="form-label-small">Apply</label><input type="number" className="styled-input" value={conf.bloomDist.Apply} onChange={e => updateSectionConfig(idx, 'bloomDist.Apply', e.target.value)} /></div>
                      <div className="form-group"><label className="form-label-small">Analyze</label><input type="number" className="styled-input" value={conf.bloomDist.Analyze} onChange={e => updateSectionConfig(idx, 'bloomDist.Analyze', e.target.value)} /></div>
                      <div className="form-group"><label className="form-label-small">Evaluate</label><input type="number" className="styled-input" value={conf.bloomDist.Evaluate} onChange={e => updateSectionConfig(idx, 'bloomDist.Evaluate', e.target.value)} /></div>
                      <div className="form-group"><label className="form-label-small">Create</label><input type="number" className="styled-input" value={conf.bloomDist.Create} onChange={e => updateSectionConfig(idx, 'bloomDist.Create', e.target.value)} /></div>
                    </div>
                  </div>
                </div>

                <div className="form-group">
                  <label className="form-label">Instructions for Students (Optional)</label>
                  <textarea className="styled-input" rows="2" style={{resize:'vertical'}} value={conf.instructions} onChange={e => updateSectionConfig(idx, 'instructions', e.target.value)} placeholder="Attempt any five questions."></textarea>
                </div>
              </div>
            );
          })}
          </div>
        </div>
        
        {/* STICKY FOOTER */}
        <div className="modal-footer" style={{background: 'white', padding: '15px 25px', borderTop: '1px solid #e2e8f0', display: 'flex', justifyContent: 'flex-end', gap: '15px'}}>
          <button className="btn-move" style={{padding: '10px 25px'}} onClick={() => setAutoGenerateModalOpen(false)}>Cancel</button>
          <button className="btn-save" style={{padding: '10px 30px'}} onClick={handleGenerate}>
            ✨ Generate Paper
          </button>
        </div>
      </div>
    </div>
  );
}

export default AutoGenerateDialog;
